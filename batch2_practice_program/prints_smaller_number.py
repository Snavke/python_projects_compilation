# prompt user to initialize the first and second number
first_number = float(input("Enter Number: "))
second_number = float(input("Enter Number: "))

# compare the value of both values entered
if first_number < second_number:
    # print the lesser number if condition is met
    print (first_number)
else: 
    # print the other number if condition is not met
    print (second_number)